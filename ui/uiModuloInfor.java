    private void ReportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReportarActionPerformed
        // TODO add your handling code here:
         // Obtener el índice seleccionado del ComboBox
    int selectedIndex = CmbTipoAtaque.getSelectedIndex();
    
    // Verificar si se seleccionó un índice válido
    if (selectedIndex >= 0) {
        // Obtener la amenaza seleccionada de la lista de amenazas usando el índice
        amenaza amenazaSeleccionada = listaAmenazas.get(selectedIndex);
        
        // Incrementar el número de ataques de la amenaza seleccionada
        amenazaSeleccionada.incrementarNumeroAtaques();
        
        // Actualizar el campo txtnumeroAtaques con el nuevo valor
        txtnumeroAtaques.setText(Integer.toString(amenazaSeleccionada.getNumeroAtaques()));
    }
    
    JOptionPane.showMessageDialog(this, "Amenza reportada"); 
        
    }//GEN-LAST:event_ReportarActionPerformed

