    private void CmbTipoAtaqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CmbTipoAtaqueActionPerformed
        // TODO add your handling code here:
        // Obtener el índice seleccionado del ComboBox
    int selectedIndex = CmbTipoAtaque.getSelectedIndex();
    
    // Verificar si se seleccionó un índice válido
    if (selectedIndex >= 0) {
        // Obtener la amenaza seleccionada de la lista de amenazas
        amenaza amenazaSeleccionada = listaAmenazas.get(selectedIndex);
        
        // Obtener la descripción de la amenaza seleccionada
        String descripcionAmenaza = amenazaSeleccionada.getDescripcionAmenaza();
        
        // Obtener el número de ataques de la amenaza seleccionada
        int numeroAtaques = amenazaSeleccionada.getNumeroAtaques();
        
        // Establecer la descripción en el campo de texto
        jTextArea1.setText(descripcionAmenaza);
        
        // Establecer el número de ataques en el campo de texto correspondiente
        txtnumeroAtaques.setText(Integer.toString(numeroAtaques));
    }
    }//GEN-LAST:event_CmbTipoAtaqueActionPerformed

